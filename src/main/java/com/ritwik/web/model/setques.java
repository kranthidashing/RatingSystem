package com.ritwik.web.model;

import java.util.Set;

public class setques {
	
	private Set<String> questions;
	private String Services;
	private String ServiceProducts;

	public Set<String> getQuestions() {
		return questions;
	}

	public void setQuestions(Set<String> questions) {
		this.questions = questions;
	}

	public String getServices() {
		return Services;
	}

	public void setServices(String services) {
		Services = services;
	}

	public String getServiceProducts() {
		return ServiceProducts;
	}

	public void setServiceProducts(String serviceProducts) {
		ServiceProducts = serviceProducts;
	}

}
