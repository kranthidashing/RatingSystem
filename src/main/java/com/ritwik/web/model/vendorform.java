package com.ritwik.web.model;

public class vendorform {
	private String fname;
	private String lname;
	private String uname;
	private String password;
	private String email;
	private String providedservices;
	private String providedserviceproducts;
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getLname() {
		return lname;
	}
	public void setLname(String lname) {
		this.lname = lname;
	}
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getProvidedservices() {
		return providedservices;
	}
	public void setProvidedservices(String providedservices) {
		this.providedservices = providedservices;
	}
	public String getProvidedserviceproducts() {
		return providedserviceproducts;
	}
	public void setProvidedserviceproducts(String providedserviceproducts) {
		this.providedserviceproducts = providedserviceproducts;
	}
	


}
